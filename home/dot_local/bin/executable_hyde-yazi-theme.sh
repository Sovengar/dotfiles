# Parse terminal.theme and extract color values
# Format: "colorN    #hexvalue" or "foreground    #hexvalue"
parse_terminal_theme() {
    local theme_file="$1"
    declare -gA COLORS

    while IFS= read -r line; do
        # Skip comment-only lines (start with #)
        [[ "$line" =~ ^[[:space:]]*# ]] && continue
        # Skip empty lines
        [[ -z "${line// /}" ]] && continue

        # Match lines like: foreground    #cdd2d3  or  color0    #2d1b3a
        # Also handle inline comments after the value: color5 #magenta
        if [[ "$line" =~ ^[[:space:]]*([a-zA-Z0-9_]+)[[:space:]]+(#[0-9a-fA-F]{6}) ]]; then
            local key="${BASH_REMATCH[1],,}"  # lowercase
            local val="${BASH_REMATCH[2]}"
            COLORS["$key"]="$val"
        # Handle lines with "none" value (like selection_foreground    none)
        elif [[ "$line" =~ ^[[:space:]]*([a-zA-Z0-9_]+)[[:space:]]+(none) ]]; then
            local key="${BASH_REMATCH[1],,}"
            COLORS["$key"]="none"
        fi
    done < "$theme_file"
}

