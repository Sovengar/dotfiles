# Get the active theme name from hyde.conf
get_active_theme() {
    if [[ -f "$HYDE_STATE_FILE" ]]; then
        grep -oP 'HYDE_THEME="\K[^"]+' "$HYDE_STATE_FILE" 2>/dev/null || true
    fi
}

# Main logic
main() {
    local theme_name=""
    local output_mode="file"
    local all_themes=false

    while [[ $# -gt 0 ]]; do
        case "$1" in
            --all) all_themes=true; shift ;;
            --stdout) output_mode="stdout"; shift ;;
            --help|-h)
                echo "Usage: hyde-yazi-theme [THEME_NAME|--all|--stdout]"
                echo ""
                echo "Generate Yazi theme.toml from a Hyde static theme."
                echo ""
                echo "Options:"
                echo "  THEME_NAME  Use specific theme (default: active theme)"
                echo "  --all       Generate for ALL installed Hyde themes"
                echo "  --stdout    Output to stdout instead of file"
                echo ""
                echo "Environment:"
                echo "  HYDE_THEMES_DIR   Hyde themes directory (default: ~/.local/share/hyde/themes)"
                echo "  YAZI_CONFIG_DIR   Yazi config directory (default: ~/.config/yazi)"
                echo "  YAZI_STATE_DIR    Yazi state directory (default: ~/.local/state/yazi)"
                echo "  HYDE_STATE_FILE   Hyde state file (default: ~/.local/state/hypr/hyde.conf)"
                exit 0
                ;;
            *) theme_name="$1"; shift ;;
        esac
    done

    if [[ "$all_themes" == true ]]; then
        mkdir -p "$YAZI_STATE_DIR/themes"
        for theme_dir in "$HYDE_THEMES_DIR"/*/; do
            local tname
            tname="$(basename "$theme_dir")"
            local theme_file="$theme_dir/terminal.theme"
            if [[ ! -f "$theme_file" ]]; then
                echo "[Yazi] Skipping '$tname' — no terminal.theme found" >&2
                continue
            fi
            echo "[Yazi] Generating theme for '$tname'..." >&2
            declare -A COLORS
            parse_terminal_theme "$theme_file"
            generate_theme > "$YAZI_STATE_DIR/themes/${tname// /_}.toml"
            unset COLORS
            declare -A COLORS
        done
        echo "[Yazi] All themes generated in $YAZI_STATE_DIR/themes/" >&2
        return 0
    fi

    if [[ -z "$theme_name" ]]; then
        theme_name="$(get_active_theme)"
        if [[ -z "$theme_name" ]]; then
            echo "[Yazi] Error: Could not detect active theme. Specify a theme name." >&2
            exit 1
        fi
    fi

    local theme_file="$HYDE_THEMES_DIR/$theme_name/terminal.theme"
    if [[ ! -f "$theme_file" ]]; then
        echo "[Yazi] Error: Theme file not found: $theme_file" >&2
        echo "[Yazi] Available themes:" >&2
        ls -1 "$HYDE_THEMES_DIR" 2>/dev/null | sed 's/^/  /' >&2
        exit 1
    fi

    declare -A COLORS
    parse_terminal_theme "$theme_file"

    if [[ "$output_mode" == "stdout" ]]; then
        generate_theme
    else
        mkdir -p "$YAZI_STATE_DIR"
        generate_theme > "$YAZI_STATE_DIR/theme.toml"
        mkdir -p "$YAZI_CONFIG_DIR"
        ln -sf "$YAZI_STATE_DIR/theme.toml" "$YAZI_CONFIG_DIR/theme.toml"
        echo "[Yazi] Theme generated: $YAZI_STATE_DIR/theme.toml → $YAZI_CONFIG_DIR/theme.toml (from '$theme_name')" >&2
    fi
}

main "$@"