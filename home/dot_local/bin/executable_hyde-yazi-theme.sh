# Reads terminal.theme from a Hyde theme directory and outputs theme.toml
#
# Usage:
#   hyde-yazi-theme.sh              # Use currently active theme
#   hyde-yazi-theme.sh "Tokyo Night"  # Use specific theme
#   hyde-yazi-theme.sh --all         # Generate for ALL themes (each in ~/.config/yazi/themes/)
#   hyde-yazi-theme.sh --stdout      # Output to stdout instead of file
#
# Environment:
#   HYDE_THEME_DIR  — Override theme directory detection
#   HYDE_STATE_FILE — Override state file path (default: ~/.local/state/hypr/hyde.conf)
#   YAZI_CONFIG_DIR — Override Yazi config directory (default: ~/.config/yazi)

